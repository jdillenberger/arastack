// isAppDeployed checks if a given app is currently deployed.
func (m *Manager) isAppDeployed(appName string) bool {
	deployed, err := m.ListDeployed()
	if err != nil {
		return false
	}
	for _, d := range deployed {
		if d == appName {
			return true
		}
	}
	return false
}

// collectAllRoutingDomains gathers all local routing domains from deployed apps.
func (m *Manager) collectAllRoutingDomains(currentApp string, mergedValues map[string]string) []string {
	domainSet := make(map[string]bool)

	dashboardDomain := m.cfg.RoutingDomain()
	if routing.IsLocalDomain(dashboardDomain) {
		domainSet[dashboardDomain] = true
	}

	if rd, ok := mergedValues["routing_domain"]; ok && rd != "" && routing.IsLocalDomain(rd) {
		domainSet[rd] = true
	}

	deployed, err := m.ListDeployed()
	if err == nil {
		for _, name := range deployed {
			if name == currentApp {
				continue
			}
			info, err := m.GetDeployedInfo(name)
			if err != nil {
				continue
			}
			if info.Routing != nil && info.Routing.Enabled {
				for _, d := range info.Routing.Domains {
					if routing.IsLocalDomain(d) {
						domainSet[d] = true
					}
				}
			}
		}
	}

	domains := make([]string, 0, len(domainSet))
	for d := range domainSet {
		domains = append(domains, d)
	}
	sort.Strings(domains)
	return domains
}

// RenewCerts checks whether the local TLS certificates are expiring and
// regenerates them if needed. When certificates are renewed it restarts
// traefik and regenerates the CA bundle for every deployed app.
func (m *Manager) RenewCerts() error {
	if !m.cfg.Routing.HTTPS.Enabled {
		return nil
	}

	cm := certs.NewManager(m.cfg.DataPath("traefik"))
	certDomains := m.collectAllRoutingDomains("", nil)
	if len(certDomains) == 0 {
		return nil
	}

	renewed, err := cm.EnsureCertsIfNeeded(certDomains)
	if err != nil {
		return fmt.Errorf("renewing certificates: %w", err)
	}
	if !renewed {
		return nil
	}

	slog.Info("Certificates renewed, restarting traefik")
	if m.isTraefikDeployed() {
		if err := m.Restart("traefik"); err != nil {
			slog.Error("Failed to restart traefik after cert renewal", "error", err)
		}
	}

	// Regenerate CA bundles for all deployed apps.
	caCertPath := cm.CACertPath()
	deployed, err := m.ListDeployed()
	if err != nil {
		return fmt.Errorf("listing deployed apps: %w", err)
	}
	for _, appName := range deployed {
		dataDir := m.cfg.DataPath(appName)
		if err := generateCABundle(caCertPath, dataDir); err != nil {
			slog.Warn("Failed to regenerate CA bundle", "app", appName, "error", err)
		}
	}

	slog.Info("CA bundles regenerated for all deployed apps")
	return nil
}

// generateCABundle creates a CA certificate bundle.
// If the local CA cert is not available (e.g. traefik not yet deployed),
// it falls back to using just the system CA bundle so the mount still works.
func generateCABundle(caCertPath, dataDir string) error {
	systemBundle, err := os.ReadFile("/etc/ssl/certs/ca-certificates.crt") // #nosec G304 -- well-known system path
	if err != nil {
		return fmt.Errorf("reading system CA bundle: %w", err)
	}

	bundle := systemBundle
	localCA, err := os.ReadFile(caCertPath) // #nosec G304 -- path is constructed internally
	if err != nil {
		slog.Warn("Local CA cert not available, using system bundle only", "path", caCertPath, "error", err)
	} else {
		bundle = make([]byte, 0, len(systemBundle)+1+len(localCA))
		bundle = append(bundle, systemBundle...)
		bundle = append(bundle, '\n')
		bundle = append(bundle, localCA...)
	}

	bundlePath := filepath.Join(dataDir, "ca-bundle.crt")
	return os.WriteFile(bundlePath, bundle, 0o644) // #nosec G306,G703 -- CA bundle is public; path is constructed internally
}

