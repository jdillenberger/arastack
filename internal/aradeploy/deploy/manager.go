// processCodeDeploy parses --code flags, clones/syncs code sources, and returns CodeSource entries.
func (m *Manager) processCodeDeploy(appName string, meta *template.AppMeta, codeFlags map[string]string) ([]code.Source, error) {
	if meta.Code == nil {
		return nil, nil
	}

	slotMap := make(map[string]template.CodeSlot)
	for _, s := range meta.Code.Slots {
		slotMap[s.Name] = s
	}

	codeMgr := code.NewManager(m.cfg.CodeDir, m.runner)
	var sources []code.Source

	for key, sourceSpec := range codeFlags {
		// Parse key: "slot" or "slot/name"
		slotName, name := parseCodeKey(key)

		slot, ok := slotMap[slotName]
		if !ok {
			return nil, fmt.Errorf("unknown code slot %q (template defines: %s)", slotName, availableSlotNames(meta))
		}

		if slot.Multiple && name == "" {
			return nil, fmt.Errorf("code slot %q supports multiple items — use %s/<name>=<source>", slotName, slotName)
		}
		if !slot.Multiple && name != "" {
			return nil, fmt.Errorf("code slot %q does not support multiple items — use %s=<source>", slotName, slotName)
		}

		if slot.InjectMode() == "build" && !meta.RequiresBuild {
			return nil, fmt.Errorf("code slot %q uses inject=build but template does not have requires_build: true", slotName)
		}

		// Parse source#branch
		source, branch := parseSourceBranch(sourceSpec)

		cs, err := codeMgr.Add(appName, slot, name, source, branch)
		if err != nil {
			return nil, fmt.Errorf("adding code for %s: %w", key, err)
		}
		sources = append(sources, cs)
	}

	return sources, nil
}

// validateRequiredCodeSlots checks that all required code slots have at least one source.
func (m *Manager) validateRequiredCodeSlots(meta *template.AppMeta, sources []code.Source) error {
	if meta.Code == nil {
		return nil
	}

	provided := make(map[string]bool)
	for _, cs := range sources {
		provided[cs.Slot] = true
	}

	for _, slot := range meta.Code.Slots {
		if slot.Required && !provided[slot.Name] {
			return fmt.Errorf("required code slot %q is not provided — use --code %s=<source>", slot.Name, slot.Name)
		}
	}
	return nil
}

// parseCodeKey splits a code key like "slot/name" into slot and name parts.
func parseCodeKey(key string) (slot, name string) {
	if idx := strings.Index(key, "/"); idx >= 0 {
		return key[:idx], key[idx+1:]
	}
	return key, ""
}

// parseSourceBranch splits a source spec like "url#branch" into source and branch.
func parseSourceBranch(spec string) (source, branch string) {
	if idx := strings.LastIndex(spec, "#"); idx >= 0 {
		return spec[:idx], spec[idx+1:]
	}
	return spec, ""
}

// availableSlotNames returns a comma-separated list of slot names from the template.
func availableSlotNames(meta *template.AppMeta) string {
	if meta.Code == nil {
		return "(none)"
	}
	var names []string
	for _, s := range meta.Code.Slots {
		names = append(names, s.Name)
	}
	return strings.Join(names, ", ")
}

// printPostDeployInfo displays post-deploy information including user-facing secrets.
// It consolidates access URL, login credentials (username from post_deploy_info +
// user_facing secret values), and setup notes into one block.
func printPostDeployInfo(meta *template.AppMeta, values map[string]string) {
	info := meta.PostDeployInfo

	// Collect all user-facing secrets that have a value.
	var secrets []struct{ name, value, desc string }
	for _, v := range meta.Values {
		if !v.UserFacing || !v.Secret {
			continue
		}
		val := values[v.Name]
		if val == "" {
			continue
		}
		secrets = append(secrets, struct{ name, value, desc string }{v.Name, val, v.Description})
	}

	if info == nil && len(secrets) == 0 {
		return
	}

	fmt.Println("--- Post-Deploy Info ---")

	if info != nil && info.AccessURL != "" {
		if rendered, err := renderTemplate(info.AccessURL, values); err == nil {
			fmt.Printf("  Access URL:    %s\n", rendered)
		}
	}

	// Show login credentials: username from post_deploy_info + user-facing secrets.
	hasCredentials := info != nil && info.Credentials != ""
	if hasCredentials || len(secrets) > 0 {
		fmt.Println()
		fmt.Println("  Login credentials:")
		if hasCredentials {
			if rendered, err := renderTemplate(info.Credentials, values); err == nil {
				fmt.Printf("    %s\n", rendered)
			}
		}
		for _, e := range secrets {
			label := e.desc
			if label == "" {
				label = e.name
			}
			fmt.Printf("    %-25s %s\n", label+":", e.value)
		}
	}

	if info != nil && len(info.Notes) > 0 {
		fmt.Println()
		fmt.Println("  Notes:")
		for i, note := range info.Notes {
			if rendered, err := renderTemplate(note, values); err == nil {
				fmt.Printf("    %d. %s\n", i+1, rendered)
			}
		}
	}

	if len(secrets) > 0 {
		fmt.Println()
		fmt.Println("  View secrets later with: aradeploy secrets " + meta.Name)
	}

	fmt.Println()
}
